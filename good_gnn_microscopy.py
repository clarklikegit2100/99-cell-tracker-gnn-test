from omegaconf import OmegaConf
import run  # this assumes your `run.py` file defines a callable `main()` function via Hydra

def run_training():
    # Set Hydra error trace mode
    os.environ["HYDRA_FULL_ERROR"] = "1"

    # Define dataset and experiment paths
    folder_path = "/Users/my505/Documents/Data/gnntest"
    data_name = "PhC-C2DH-U373"
    main_path = os.path.join(folder_path, "ct_features", data_name)

    # Create config dict
    config_dict = {
        "datamodule": {
            "_target_": "src.datamodules.celltrack_datamodule.CellTrackDataModule",
            "dataset_params": {
                "main_path": main_path,
                "exp_name": "2D_SIM",
                "drop_feat": [],
                "num_frames": 3,
                "type_file": "train",
                "dirs_path": {
                    "train": [os.path.join(main_path, "01", "csv")],
                    "val": [os.path.join(main_path, "02", "csv")]
                },
                "edge_feat_embed_dict": os.path.join(folder_path, "software", "parameters", "embed_dict.pkl"),
                # adjust if missing
                "normalize_all_cols": True
            }
        },
        "trainer": {
            "fast_dev_run": False
        }
    }

    # Convert to OmegaConf DictConfig
    config = OmegaConf.create(config_dict)
    OmegaConf.set_struct(config, False)  # ðŸ”‘ This line is essential

    # Call Hydra-decorated entry point (assuming run.main exists and takes config)
    print("Starting training...")
    run.main(config)
    print("Training finished.")


if __name__ == "__main__":
    #run_feature_extraction()
    run_training()